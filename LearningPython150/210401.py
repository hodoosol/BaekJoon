# 4892
i = 1
while True :
    n = int(input())
    if n == 0 :
        break
    if n % 2 == 0 :
        print('{0}. even'.format(i), end=' ')
        n //= 2
    else :
        n = (n - 1) // 2
        print('{0}. odd'.format(i), end=' ')
    print(n)
    i += 1





